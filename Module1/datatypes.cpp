#include <iostream>
using namespace std;

int main() {
   // Integer types
   int age = 21;
   unsigned int score = 100;

   // Floating-point types
   float temperature = 36.6f;
   double pi = 3.14159265359;

   // Character type
   char grade = 'A';

   // Boolean type
   bool isStudent = true;

   // Output values
   cout << "Age: " << age << endl;
   cout << "Score: " << score << endl;
   cout << "Temperature: " << temperature << endl;
   cout << "Pi: " << pi << endl;
   cout << "Grade: " << grade << endl;
   cout << "Is student: " << isStudent << endl;

   return 0;
}
